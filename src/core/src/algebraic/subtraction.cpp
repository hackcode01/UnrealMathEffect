fast_u8 subtractiond(fast_u8 a, fast_u8 b) {
    return a - b;
}

fast_u16 subtraction(fast_u16 a, fast_u16 b) {
    return a - b;
}

fast_u32 subtraction(fast_u32 a, fast_u32 b) {
    return a - b;
}

fast_u64 subtraction(fast_u64 a, fast_u64 b) {
    return a - b;
}
